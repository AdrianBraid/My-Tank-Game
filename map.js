//a function for initiliaizng the level, once a new level has started 
function initializeLevel(coin) {
  //sets the tank initial position
  for (e of userTankPositions[mapNumber]) {
    tank1.x = e.x
    tank1.y = e.y
  }
  enemies = []
  //adding enemies into the map, including their initial position 
  for (p of enemyPositionL1[mapNumber]) {
    enemies.push(new Tank(p.x, p.y, 40, 40, enemyTank1, enemyTurret, false))
  }
  //allow the upgrade menu to be shown
  showUpgradeMenu = true
  //set the map to the approrpiate level
  currentMap = maps[mapNumber]
  //if a new level has been started, spawn in a new set of coins.
  if (coin) {
    coinGen()
  }
  //if a level is being restet, remove the original coins and spawn in new coins.
  if(!coin){
    resetCoins()
    coinGen()
  }
}

// a function for removing the coins on a map 
function resetCoins() {
  imageMode(CORNER)
  //a loop for going through the x and y coordinates of the grid, rmeoving all coins from the map.
  for (let y = 0; y < currentMap.length; y++) {
    for (let x = 0; x < currentMap[0].length; x++) {
      if(currentMap[y][x] == 2){
        currentMap[y][x] = 0
      }
    }
  }
}
//A function for initializing a level for multiplayer uses.
function initializeMultiplayerLevel() {
  //sets the death of the two tanks to false, so that they are shown.
  tank1.death = false
  enemyTank.death = false
  //goes through the user tank positoin array, looping through each individual level and obtianing its x and y position for player1
  for (e of userTankPositions[mapNumber]) {
    tank1.x = e.x
    tank1.y = e.y
  }
  //goes through the initial positions for the enemy tank position array, looping through each individual level and obtaining the x and y values for player 2s positions for the new level.
  for (m of enemyTankPositions[mapNumber]) {
    enemyTank.x = m.x
    enemyTank.y = m.y
  }
  //sets the map to the appropriate level
  currentMap = maps[mapNumber]
}

//a function for spawning in coins in the map.
function coinGen() {
  console.log("coins")
  coinNum = 0
  //create random x and y grid positions 20 times
  for (i = 0; i <= 20; i++) {
    RandomiseX = Math.round(random(0, 19))
    RandomiseY = Math.round(random(0, 19))
    //checking that the coin isnt a wall, nor cobweb, only setting a limit to spawn the coin 20 times. 
    if (RandomiseX != 3 && RandomiseY != 2 && coinNum <= 20 && currentMap[RandomiseY][RandomiseX] != 1 && currentMap[RandomiseY][RandomiseX] != 3) {
      //spawning in the coin
      currentMap[RandomiseY][RandomiseX] = 2
      coinNum++
    }
  }
}

function drawGrid() {
  //for drawing the grid in the game.
  imageMode(CORNER)
  //loop through each grid coordinate, drawing the appropipate image (wall/cobweb/floor)
  for (let y = 0; y < currentMap.length; y++) {
    for (let x = 0; x < currentMap[0].length; x++) {
      image(imgs[currentMap[y][x]], sqSize * x, sqSize * y, sqSize, sqSize)
    }
  }
}